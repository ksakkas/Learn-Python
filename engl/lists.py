# List in Python

ls = ["dit", "uoi", "arta"]
print(ls)

print("------------------------------------------")

print(ls[0])
print(ls[1])
print(ls[2])

print("------------------------------------------")

print(ls[-1])

print("------------------------------------------")

print(ls[0:2])

print("------------------------------------------")

ls.insert(1, "ioannina")    # Insert a item in list
print(ls)

print("------------------------------------------")

ls.remove("ioannina")   # Remove a item in list
print(ls)

print("------------------------------------------")

k = ls.copy()   # Copy list
print(k)

