# Παραγωγή Τυχαίου Αριθμού
# ΠΡΟΣΟΧΗ: μην ονομάσετε το αρχείο random.py

# Η Python δεν έχει μια rundom() συνάρτηση για να κάνει έναν τυχαίο αριθμό, 
# αλλά η Python έχει μια ενσωματωμένη μονάδα που ονομάζεται random και μπορεί 
# να χρησιμοποιηθεί για την παραγωγή τυχαίων αριθμών:

import random  # Εισαγωγή της μονάδας random

print(random.randrange(1, 5))  # Παραγωγή τυχαίου αριθμού στο εύρος 1 εώς 5

