# IF ELSEIF ELSE
# Η Python βασίζεται στην εσοχή (κενό διάστημα στην αρχή μιας γραμμής) για να καθορίσει το πεδίο 
# εφαρμογής στον κώδικα. Άλλες γλώσσες προγραμματισμού χρησιμοποιούν συχνά αγκύλες για το σκοπό αυτό.

a = 5
b = 10
if b > a:
    print("b > a")
elif a == b:
    print("a == b")
else:
    print("a < b")
